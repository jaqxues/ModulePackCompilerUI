public class D8WrapperException extends Exception {
    public D8WrapperException() {
        super();
    }

    public D8WrapperException(String message) {
        super(message);
    }

    public D8WrapperException(String message, Throwable cause) {
        super(message, cause);
    }

    public D8WrapperException(Throwable cause) {
        super(cause);
    }

    protected D8WrapperException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
